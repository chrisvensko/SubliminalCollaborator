# this is a collection of mock sublime api functions and classes

class Region(object):

    def __init__(self, a, b):
        self.a = a
        self.b = b

    def begin(self):
        return self.a

    def end(self):
        return self.b

    def size(self):
        return abs(self.b - self.a)

    def empty(self):
        return self.a == self.b
